package com.example.ZZfishing.api.profile.controller.dto;

import lombok.Data;


public class ProfileDto {

    private long Id;
    private String name;
    private String email;

    public long getId() {
        return Id;
    }

    public void setId(long id) {
        Id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
